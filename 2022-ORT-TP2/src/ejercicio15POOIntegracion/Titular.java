package ejercicio15POOIntegracion;

public class Titular {
	private String DNI, nombre, apellido, telefono, mail;

	public Titular(String dNI, String nombre, String apellido, String telefono, String mail) {
		super();
		DNI = dNI;
		this.nombre = nombre;
		this.apellido = apellido;
		this.telefono = telefono;
		this.mail = mail;
	}

	public String getNombre() {
		return nombre;
	}


	public String getApellido() {
		return apellido;
	}



	@Override
	public String toString() {
		return "Titular [DNI=" + DNI + ", nombre=" + nombre + ", apellido=" + apellido + ", telefono=" + telefono
				+ ", mail=" + mail + "]";
	}
	
    public String nombreCompleto() {
        return nombre + " " + apellido;
    }
	
	
}
